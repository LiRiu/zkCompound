import fss from 'fs';

export class MarineProcessor {
  constructor (marineAddress) {
    this.balances = []
    this.principals = []
    this.marineAddress = marineAddress
    const data = fss.readFileSync('build/' + marineAddress + '.json', 'utf8')
    this.markets = JSON.parse(data)
  }

  run () {
    for (const key in this.markets) {
      const market = this.markets[key]
      this.balances.push(Number(market.balance))
      this.principals.push(Number(market.principal))
    }

    let resultString = `//The code is generated by api/marine.js\nexport const balances: number[] = ${JSON.stringify(this.balances)}\n`
    resultString += `export const principals: number[] = ${JSON.stringify(this.principals)}\n`

    fss.writeFileSync('src/static/marine.ts', resultString)
    console.log(this.marineAddress + ' processed')
  }
}

